///Convertir de metros a decametros
#include <bits/stdc++.h>
using namespace std;
int main()
{
    float metros;
    cout<<"ingrese metros:";
    cin>>metros;
    float decam=metros/10;
    float hectom=metros/100;
    float kilom=metros/1000;
    cout<<"el resultado en decametro ="<<decam<<endl;
    cout<<"el resultado es hectometro ="<<hectom<<endl;
    cout<<"el resultado es kilometro ="<<kilom<<endl;
    return 0;
}
